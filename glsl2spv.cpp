void DefaultLogger(const char *message){
    printf("[glsl2spv]: %s\n", message);
}

bool CompileGLSL2SPV(const char *sources, size_t sources_length, ShaderType type, std::vector<std::uint32_t> &out_binary, LogProc logger){
	// Starts converting GLSL to SPIR-V.
	auto language = s_LangTable[(size_t)type];
	glslang::TProgram program;
	glslang::TShader shader(language);
	auto &resources = DefaultTBuiltInResource;

	// Enable SPIR-V and Vulkan rules when parsing GLSL.
	auto messages = static_cast<EShMessages>(EShMsgSpvRules | EShMsgVulkanRules | EShMsgDefault);

	auto shaderNameCstr = s_LangNameTable[(size_t)type];
	auto shaderSource = sources;
	shader.setStringsWithLengthsAndNames(&shaderSource, nullptr, &shaderNameCstr, 1);

	auto defaultVersion = glslang::EShTargetVulkan_1_1;
	shader.setEnvInput(glslang::EShSourceGlsl, language, glslang::EShClientVulkan, 110);
	shader.setEnvClient(glslang::EShClientVulkan, defaultVersion);
	shader.setEnvTarget(glslang::EShTargetSpv, glslang::EShTargetSpv_1_0);

	if (!shader.parse(&resources, defaultVersion, true, messages)) {
		logger(shader.getInfoLog());
		logger(shader.getInfoDebugLog());
		logger("SPRIV shader parse failed!");
	}

	program.addShader(&shader);

	if (!program.link(messages) || !program.mapIO()) {
		logger("Error while linking shader program.");
	}

	glslang::SpvOptions spvOptions;
#if defined(ACID_DEBUG)
	spvOptions.generateDebugInfo = true;
	spvOptions.disableOptimizer = true;
	spvOptions.optimizeSize = false;
#else
	spvOptions.generateDebugInfo = false;
	spvOptions.disableOptimizer = false;
	spvOptions.optimizeSize = true;
#endif

	spv::SpvBuildLogger log;
	GlslangToSpv(*program.getIntermediate(static_cast<EShLanguage>(language)), out_binary, &log, &spvOptions);
    auto message = log.getAllMessages();
    if(message.size())
        logger(message.c_str());
    return !message.size() && out_binary.size();
}

